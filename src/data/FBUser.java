import java.net.URL;

public class FBUser {

    private String id;
    private URL url;
    private Image picSquare;

    public FBUser () {
    }

    
    
    public String getId () {
        return id;
    }

    public Image getPicSquare () {
        return picSquare;
    }

    public URL getUrl () {
        return url;
    }

}

